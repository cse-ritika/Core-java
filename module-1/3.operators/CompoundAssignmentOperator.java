class CompoundAssignmentOperator{
	public static void main(String[] args) {
		int i=25;
		System.out.println("Content of i : "+i);
		i+=10; // i=i+10
		System.out.println("After addition the content of i : "+i);
		i-=5;
		System.out.println("After subtraction the content is : "+i);
		i*=2;
		System.out.println("After multiplication the content is : "+i);
		i/=2;
		System.out.println("After division the content is : "+i);
	}
}