class ArithmeticOperation{
	public static void main(String[] args) {
		int a=10;
		System.out.println("Content of a is : "+a);
		int b=5;
		System.out.println("Content of b is : "+b);
		int c=a+b;
		System.out.println("The addition of "+a+" and "+b+" is : "+c);

		int d = a-b;
		System.out.println("The subtraction of "+a+" and "+b+" is : "+d);

		int e=a/b;
		System.out.println("The division of "+a+" and "+b+" is : "+e);

		int f = a%b;
		System.out.println("The modules of "+a+" and "+b+" is : "+f);

		int g=a*b;
		System.out.println("The multiplication of "+a+" and "+b+" is : "+g);
	}
}