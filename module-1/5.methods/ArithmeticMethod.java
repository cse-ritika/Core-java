class ArithmeticMethod{
	public static void addition(int n1,int n2){
		System.out.println("content of n1 is : "+n1);
		System.out.println("content of n2 is : "+n2);
		int a = n1+n2;
		System.out.println("Addition of "+n1+" and "+n2+" is : "+a);
		System.out.println();
	}
	public static void subtraction(int n1,int n2){
		System.out.println("content of n1 is : "+n1);
		System.out.println("content of n2 is : "+n2);
		int b = n1-n2;
		System.out.println("Subtraction of "+n1+" and "+n2+" is : "+b);
		System.out.println();
	}
	public static void division(int n1,int n2){
		System.out.println("content of n1 is : "+n1);
		System.out.println("content of n2 is : "+n2);
		int c = n1/n2;
		System.out.println("Division of "+n1+" and "+n2+" is : "+c);
		System.out.println();
}
public static void multiplication(int n1,int n2){
		System.out.println("content of n1 is : "+n1);
		System.out.println("content of n2 is : "+n2);
		int d = n1*n2;
		System.out.println("Multiplication of "+n1+" and "+n2+" is : "+d);
		System.out.println();
}
}